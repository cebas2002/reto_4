    public void recibeDtosDirecion(int idDireccion){
        String sucursal =txtSucursal.getText();
        String queryVer = "INSERT INTO `sucursal`(`nombreSucursal`, `FK_nit`, `FK_idDireccion`) VALUES ('"+ sucursal +"','999999990',"+ idDireccion +");";
        try {
            connection = conexion.getConnection();
            st = connection.createStatement();
            st.executeUpdate(queryVer);
            JOptionPane.showMessageDialog(this,"Sucursal creada exitosamente ");
        } catch (Exception e) {
            System.out.println(e);
        }
        System.out.println(queryVer);
    }
    
    
    
    
    
    
