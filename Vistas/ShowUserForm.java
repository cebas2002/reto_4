    public void ActualizarEmpleado() {
        String documento = txtDocumento.getText();
        String queryIdEmpleado = "SELECT idEmp FROM empleado WHERE docimento = '" + documento + "';";
        try {
            connection = connexion.getConnection();
            st = connection.createStatement();
            rs = st.executeQuery(queryIdEmpleado);
            while (rs.next()){
                int idEmpleado = rs.getInt("idEmp");
                String nombre = txtNombre.getText();
                String apellidos = txtApellidos.getText();
                String correo = txtCorreo.getText();
                if (nombre.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "el nombre es requerido", " ", JOptionPane.WARNING_MESSAGE);
                } else if (apellidos.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "el apellido es requerido", " ", JOptionPane.WARNING_MESSAGE);
                } else if (correo.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "el correo es requerido", " ", JOptionPane.WARNING_MESSAGE);
                } else {
                    String query = "UPDATE `empleados` SET `nombreEmp`='" + nombre + "',`apellidos`='" + apellidos + "',`correo`='" + correo
                            + "' WHERE `idEmp` = " + idEmpleado + ";";}
                
                    try{
                        connection = connexion.getConnection();
                        st = connection.createStatement();
                        rs = st.executeQuery(queryIdEmpleado);
                        JOptionPane.showMessageDialog(this, "se ha actualizado los datos ", " ", JOptionPane.WARNING_MESSAGE);
                        this.dispose();
                    }
                    
                    catch (Exception e) {}
                    JOptionPane.showMessageDialog(this, "no ha actualizado los datos ", " ", JOptionPane.WARNING_MESSAGE);
            }
            
        } catch (Exception e) {
        }
    }
            

    public void EliminarEmpleado() {
        String documento = txtDocumento.getText();
        String queryIdEmpleado = "SELECT idEmp FROM empleado WHERE docimento = '" + documento + "';";
        try {
            connection = connexion.getConnection();
            st = connection.createStatement();
            rs = st.executeQuery(queryIdEmpleado);
            while (rs.next()) {                
                int idEmpleado = rs.getInt("idEmp");
                String queryEliminar = "DELETE FROM empleado WHERE documento = '"+documento+"';";
                try {
                    st.executeUpdate(queryEliminar);
                    JOptionPane.showMessageDialog(this, "el empleado ha sido eliminado");
                    this.dispose();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "no ha eliminado el empleado ", " ", JOptionPane.WARNING_MESSAGE);
                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

